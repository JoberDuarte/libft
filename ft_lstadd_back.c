#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*temp;

	temp = *lst;
	if (!temp)
		return ;
	if (temp == 0)
		return ;
	while (temp->next)
		temp = temp->next;
	temp->next = new;
	new->next = NULL;
}
